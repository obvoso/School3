
public class TypeMismatch {
    public static void main(String[] args) {
        String title = "로미오와 줄리엣";
        String author = "윌리엄 셰익스피어";
        double price = 5.94;

        System.out.printf("제목: %s\n ", title);
        System.out.printf("저자: %s\n", author);
        System.out.printf("가격: $%f\n", price);
    }
}
